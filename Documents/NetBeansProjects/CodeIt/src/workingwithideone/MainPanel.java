    /**
     * Opens a user defined file and returns its contents
     *
     * @return
     */
    public String openFile() {
        String contents = "";
        JFileChooser ref1 = new JFileChooser();
        int showOpenDialog = ref1.showOpenDialog(null);
        if (showOpenDialog == JFileChooser.APPROVE_OPTION) {
            inputFile = ref1.getSelectedFile();
            //System.out.println(inputFile);
            BufferedReader in = null;
            try {
                in = new BufferedReader(new FileReader(inputFile));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(MainPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

            String line;
            try {
                while ((line = in.readLine()) != null) {
                    //System.out.println(line);
                    contents += line;
                    contents += "\n";
                }
            } catch (IOException ex) {
                Logger.getLogger(MainPanel.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Unable to open " + inputFile, "Error", JOptionPane.ERROR_MESSAGE);
                return "";
            }
        }
        return contents;
    }

