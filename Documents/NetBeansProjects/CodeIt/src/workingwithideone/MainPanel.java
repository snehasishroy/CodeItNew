    /**
     * Returns the selected JRadioButton in the JButtonGroup
     *
     * @return Selected JRadioButton
     */
    public JRadioButton getSelection() {
        for (Enumeration e = buttonGroup1.getElements(); e.hasMoreElements();) {
            JRadioButton b = (JRadioButton) e.nextElement();
            if (b.getModel() == buttonGroup1.getSelection()) {
                return b;
            }
        }
        return null;
    }

    /**
     * Display the SubmissionQuery panel and start a background thread
     *
     * @param evt Not used
     */
    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // retrieve src code, input file, id of programming language, public/private, executable
        // display inderminate progress bar
        // show the output panel
        Integer key = null;
        boolean privateEnabled = false;
        boolean enabled = false;


        if (jList2.isSelectionEmpty()) { //If no programming language is selected
            JOptionPane.showMessageDialog(this, "Please select a language !!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String value = jList2.getSelectedValue().toString();
            for (Iterator it = listOfLanguages.entrySet().iterator(); it.hasNext();) {
                Map.Entry entry = (Map.Entry) it.next();
                if (value.equals(entry.getValue())) {

                    // Find the key which corresponds to the selected language
                    key = (Integer) entry.getKey();
                    break; //breaking because its one to one map
                }
            }
            //System.out.println("got key from value in hashtable key:  " + key + " value: " + value);
            String selected;
            selected = jList2.getSelectedValue().toString();

            JRadioButton selection = getSelection();
            if (selection.equals(jRadioButton4)) { //If private JRadioButton is selected
                privateEnabled = true;
            }
            //System.out.println(privateEnabled);

            // Check the state of the JCheckBox
            // If it's not selected set enabled to false
            Object[] selectedObjects = jCheckBox2.getSelectedObjects();
            if (selectedObjects != null) {
                enabled = true;
            }
            //System.out.println(enabled);

            if ("".equals(sourceCode)) { // if user has not uploaded any file
                sourceCode = jEditorPane1.getText();
            }
            if ("".equals(input)) {
                input = jEditorPane2.getText();
            }
            //Set the query panel and pass all the parameters required for 
            //createSubmission() method of Ideone API
            ref.displayStatus(Authentication.userName, Authentication.password, sourceCode, key, input, enabled, privateEnabled);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

