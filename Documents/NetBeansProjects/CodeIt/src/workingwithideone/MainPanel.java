    /**
     * Sets the Jlist to display the list of programming languages
     *
     * @param table
     */
    public void setListItems(Hashtable table) {
        String[] arrays = new String[table.size()];
        Enumeration id;
        id = table.keys();
        int i = 0;
        while (id.hasMoreElements()) {
            arrays[i] = (String) (table.get((Integer) id.nextElement()));
            i++;
        }

        //Sorting the programming languages lexicographically
        int right = arrays.length;
        String temp;
        for (i = 0; i < right; i++) {
            for (int j = i + 1; j < right; j++) {
                if (arrays[j].compareToIgnoreCase(arrays[i]) < 0) {
                    temp = arrays[i];
                    arrays[i] = arrays[j];
                    arrays[j] = temp;
                }
            }
        }
        for (i = 0; i < arrays.length; i++) {
            model.addElement(arrays[i]);
        }
    }

